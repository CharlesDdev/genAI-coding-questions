# Add them together and store the result in a variable called total_score

# Print "New High Score!" if total_score is greater than or equal to 200

# Otherwise, print "Keep trying!"'''

points_level1 = 120
points_level2 = 95
bonus = 30
total_score = points_level1 + points_level2 + bonus 

if total_score >= 200:
    print("New High Score!")
else:
    print("Keep trying!")